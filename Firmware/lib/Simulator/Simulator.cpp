   _iS1Pin = iS1Pin;
   //pinMode(_iS1Pin, OUTPUT);
   //digitalWrite(_iS1Pin, LOW);
   _iS2Pin = iS2Pin;
   //pinMode(_iS2Pin, OUTPUT);
   //digitalWrite(_iS2Pin, LOW);
